object MathOperations {
    fun add(left: Int, right: Int): Int = left + right

    fun subtract(left: Int, right: Int): Int = left - right

    fun multiply(left: Int, right: Int): Int = left * right

    fun divide(left: Int, right: Int): Int = left / right
}
